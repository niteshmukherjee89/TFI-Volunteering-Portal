package com.tfi.vp.entity;

public class Opportunities {

	String oppId;
	String oppTitle;
	String oppDescription;
	String oppStartDate;
	String oppEndDate;
	String oppDailyHrs;
	String oppLocationArea;
	String oppLocationCity;
	String oppLocationState;
	String oppCreatorEmail;
	
	
	public String getOppId() {
		return oppId;
	}
	public void setOppId(String oppId) {
		this.oppId = oppId;
	}
	public String getOppTitle() {
		return oppTitle;
	}
	public void setOppTitle(String oppTitle) {
		this.oppTitle = oppTitle;
	}
	public String getOppDescription() {
		return oppDescription;
	}
	public void setOppDescription(String oppDescription) {
		this.oppDescription = oppDescription;
	}
	public String getOppStartDate() {
		return oppStartDate;
	}
	public void setOppStartDate(String oppStartDate) {
		this.oppStartDate = oppStartDate;
	}
	public String getOppEndDate() {
		return oppEndDate;
	}
	public void setOppEndDate(String oppEndDate) {
		this.oppEndDate = oppEndDate;
	}
	public String getOppDailyHrs() {
		return oppDailyHrs;
	}
	public void setOppDailyHrs(String oppDailyHrs) {
		this.oppDailyHrs = oppDailyHrs;
	}
	public String getOppLocationArea() {
		return oppLocationArea;
	}
	public void setOppLocationArea(String oppLocationArea) {
		this.oppLocationArea = oppLocationArea;
	}
	public String getOppLocationCity() {
		return oppLocationCity;
	}
	public void setOppLocationCity(String oppLocationCity) {
		this.oppLocationCity = oppLocationCity;
	}
	public String getOppLocationState() {
		return oppLocationState;
	}
	public void setOppLocationState(String oppLocationState) {
		this.oppLocationState = oppLocationState;
	}
	public String getOppCreatorEmail() {
		return oppCreatorEmail;
	}
	public void setOppCreatorEmail(String oppCreatorEmail) {
		this.oppCreatorEmail = oppCreatorEmail;
	}
	
}
