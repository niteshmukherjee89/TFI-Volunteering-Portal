package com.tfi.vp.entity;

public class Volunteer {

	String regId;
	String oppId;
	String volEmail;
	String volMobile;
	String volHrs;
	String volReason;
	public String getRegId() {
		return regId;
	}
	public void setRegId(String regId) {
		this.regId = regId;
	}
	public String getOppId() {
		return oppId;
	}
	public void setOppId(String oppId) {
		this.oppId = oppId;
	}
	public String getVolEmail() {
		return volEmail;
	}
	public void setVolEmail(String volEmail) {
		this.volEmail = volEmail;
	}
	public String getVolMobile() {
		return volMobile;
	}
	public void setVolMobile(String volMobile) {
		this.volMobile = volMobile;
	}
	public String getVolHrs() {
		return volHrs;
	}
	public void setVolHrs(String volHrs) {
		this.volHrs = volHrs;
	}
	public String getVolReason() {
		return volReason;
	}
	public void setVolReason(String volReason) {
		this.volReason = volReason;
	}
	
}
